    <h1 class="text-xl lg:text-4xl font-semibold">What is blood?</h1>
    <p class="mt-8 font-medium text-sm lg:text-base">Blood is a constantly circulating fluid providing the body with nutrition, oxygen, and waste removal. Blood is mostly liquid, with numerous cells and proteins suspended in it, making blood “thicker” than pure water. The average person has about 5 liters (more than a gallon) of blood.</p>
    <p class="mt-5 font-medium text-sm lg:text-base">A liquid called plasma makes up about half of the content of the blood. Plasma contains proteins that help the blood to clot, transport substances through the blood, and perform other functions. Blood plasma also contains glucose and other dissolved nutrients.</p>
    <p class="mt-5 font-medium text-sm lg:text-base">About half of the blood volume is composed of blood cells:</p>
    <div class="mt-5 font-medium ml-6 text-sm lg:text-base">
        <li>Red blood cells, which carry oxygen to the tissues</li>
        <li>White blood cells, which fight infections</li>
        <li>Platelets, smaller cells that help the blood to clot</li>
    </div>
    <p class="mt-5 font-medium text-sm lg:text-base">Blood is conducted through blood vessels (arteries and veins). Blood is prevented from clotting in the blood vessels by their smoothness, and the finely tuned balance of clotting factors.</p>
